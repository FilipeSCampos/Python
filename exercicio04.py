#Faça um Programa que peça as 4 notas bimestrais e mostre a média.
def media(x,y,z,w):
    print((x + y + z + w)/4)

nota1 = int(input('Quala sua primeira nota'))
nota2 = int(input('Quala sua segunda nota'))
nota3 = int(input('Quala sua terceira nota'))
nota4 = int(input('Quala sua quarta nota'))

media(nota1,nota2,nota3,nota4) 